var isOn = true;
var isHot = false;

if(isOn) {
	console.log("Man, that light is really bright.");
}

if(isOn === true) {
	console.log("Dude, that's so cool!");
}

if(isOn && isHot) {
	console.log("Wow, it is incredibly hot. Please kill the spotlights.");
}

if(isOn || isHot) {
	console.log("It's hot or the lights are on, dude.");
}

if(!isHot) {
	console.log("Dude, it's not that hot!");
}